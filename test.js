const bbStdDev = 2;  // Desvio padr√£o de 2
const rsiPeriod = 14; // RSI de 14 per√≠odos

// Saldo inicial
let usdBalance = 50; // Saldo inicial em USD
let btcBalance = 0;     // Saldo inicial em BTC
const tradeFee = 0.001; // Taxa de corretagem (0.1%)

// Fun√ß√£o para calcular os indicadores
function calculateIndicators(closes) {
    // Calcula a EMA 9
    const ema = EMA.calculate({ period: emaPeriod, values: closes });

    // Calcula as Bandas de Bollinger
    const bb = BollingerBands.calculate({
        period: bbPeriod,
        values: closes,
        stdDev: bbStdDev
    });

    // Calcula o RSI 14
    const rsi = RSI.calculate({ period: rsiPeriod, values: closes });

    return { ema, bb, rsi };
}

// Fun√ß√£o para simular compras e vendas
